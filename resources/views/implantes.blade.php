			<h2> ¿Qu&eacute; son los Implantes Dentales?</h2>
			<img class="foto2" src="https://www.vitaldent.com/blog/wp-content/uploads/2020/03/implante-dental.jpg">
			<img class="foto" src="https://www.institutoclinicasicilia.es/wp-content/uploads/diferencia-implantes-dentales.jpg">
			<br>
			<p>
				Son elementos met&aacute;licos que se ubican quir&uacute;rgicamente en los huesos maxilares, debajo de las enc&iacute;as. 
				Son una alternativa muy eficaz y segura para sustituir un diente que se pierde, independientemente si el motivo es por caries, enfermedad 
				periodental o accidente.
				<br>
				<br>
				Los implantes son como una ra&iacute;z artificial compuesta de titano biocompatible que se unen al hueso maxiofacial mediante 
				un proceso de osteointegraci&oacute;n. Una vez conseguido la uni&oacute;n se coloca el pilar y despu&eacute;s el diente.
				<br>
				<br>
				La cirug&iacute;a de implantes dentales puede ofrecer una alternativa bien aceptada a las dentaduras o a los puentes que no se 
				fijan bien y pueden ofrecer una opci&oacute;n cuando una falta de los dientes naturales no permite la creaci&oacute;n de 
				dentaduras o reemplazos de dientes con puentes.
			</p>
		</div>
