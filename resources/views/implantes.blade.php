			<br>
			<p>
				En la cl&iacute;nica Dental&amp;Fresh realizamos los siguientes tratamientos:
				<br>
				<br>
				<ol>
					<li> Implantes Endo&oacute;seos: Es un implante quir&uacute;gicamente de forma directa en los huesos maxilares. 
					Una vez que el tejido circundante ha cicatrizado, se requiere de una segunda cirug&iacute;a para conectar un poste 
					sobre el implante original. Finalmente, se coloca un diente artificial (o varios) sobre el poste. Los dientes ser&aacute;n 
					individuales, o estar&aacute;n agrupados en un puente o pr&oacute;tesis fija.</li>
					<br>
					<li> Implante Subperi&oacute;sticos: Consiste en una especie de montura que se coloca en los huesos maxilares justo debajo 
					de la enc&iacute;a. Con la cicatrizaci&oacute;n de la enc&iacute;as, la montura se fija al hueso maxilar. Los postes, que 
					se colocan en la montura, sobresalen a trav&eacute;s de las enc&iacute;as. Al igual que con el anterior implante, los dientes 
					artificiales se colocan sobre esos postes.</li>
				</ol>
