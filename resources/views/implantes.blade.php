		<div>
			<h2>¿ En que momento son necesarios ?</h2>
			<img class="foto" src="https://soluciondental.pe/wp-content/uploads/2018/12/preservar-dientes-con-implantes.gif">
			<img class="foto2" src="https://drasandragarcia.com/wp-content/uploads/2018/12/jornada-academica-implantes-bicon-870x500.jpg">
			<br>
			<p>
				Por lo general, los implantes dentales podr&iacute;an ser adecuados para ti si:
				<br>
				<br>
				<ol>
					<li> Tienes uno o m&aacute;s dientes faltantes.</li>
					<li> Tienes una mand&iacute;bula que lleg&oacute; a su crecimiento completo.</li>
					<li> Tienes huesos adecuados que aseguran los implantes o pueden recibir un injerto &oacute;seo.</li>
					<li> Tienes tejidos orales saludables.</li>
					<li> No puedes o no quieres usar dentaduras postizas.</li>
					<li> Quieres mejorar tu habla.</li>
				</ol>
			</p>
			<p>
				<br>
				Para recibir implantes, es necesarios tener enc&iacute;as sanas y hueso adecuado que soporte el implante. 
				La higiene bucal meticulosa y las visitas regulares odontol&oacute;gicas son esenciales para que los implantes 
				dentales sean exitosos a largo plazo.
			</p>
