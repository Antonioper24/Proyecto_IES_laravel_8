						El Blanquiamiento: Es un procedimiento que se realiza con el objetivo de aclarar el color de los dientes 
						cuando estos se han vuelto oscuros y amarillentos.
					</li>
					<li>
						La Carillas: Son unas finas l&aacute;minas de composite o porcelana que se colocan en la cara visible del 
						diente para mejorar aspectos como su posici&oacute;n, color, tama&ntilde;o o forma.
					</li>
